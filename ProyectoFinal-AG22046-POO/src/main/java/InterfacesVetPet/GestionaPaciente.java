    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // Obtener los valores de los JTextField
    String nombrePaciente = Txt1.getText();
    Date fechaCita = obtenerFechaDesdeTextField(Txt2);
    String motivoCita = Txt3.getText();
    String diagnostico = Txt4.getText();
    List<String> medicamentos = Arrays.asList(Txt5.getText()); 
    double cobro = Double.parseDouble(Txt6.getText());

    // Crear un nuevo objeto Expediente con los datos ingresados
    Expediente nuevoExpediente = new Expediente(nombrePaciente, fechaCita, motivoCita, diagnostico, medicamentos, cobro);

    // Guardar el nuevo expediente en el archivo
    nuevoExpediente.guardarExpedienteEnArchivo();
    
    // Limpiar los campos después de guardar
    Txt1.setText("");
    Txt2.setText("");
    Txt3.setText("");
    Txt4.setText("");
    Txt5.setText("");
    Txt6.setText("");
}

private Date obtenerFechaDesdeTextField(javax.swing.JTextField textField) {
    try {
        return new SimpleDateFormat("yyyy-MM-dd").parse(textField.getText());
    } catch (ParseException e) {
        e.printStackTrace();
        return null;
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

