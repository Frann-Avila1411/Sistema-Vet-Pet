    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
       // Hacer visible el textPaneRegistros
    textPaneRegistros.setVisible(true);

    // Obtener el último expediente desde el archivo
    List<Expediente> expedientes = Expediente.cargarExpedientesDesdeArchivo();

    // Limpiar el textPane antes de mostrar nuevos registros
    textPaneRegistros.setText("");

    // Verificar si hay al menos un expediente
    if (!expedientes.isEmpty()) {
        // Obtener el último expediente
        Expediente ultimoExpediente = expedientes.get(expedientes.size() - 1);

        // Formatear la información detallada del último expediente (simulando una factura)
        String expedienteFormateado = String.format(
                "Nombre del Paciente: %s\n" +
                        "Fecha de Cita: %s\n" +
                        "Motivo de Cita: %s\n" +
                        "Diagnóstico: %s\n" +
                        "Medicamentos: %s\n" +
                        "Cobro: %.2f\n\n",
                ultimoExpediente.getNombrePaciente(), ultimoExpediente.getFechaCita(),
                ultimoExpediente.getMotivoCita(), ultimoExpediente.getDiagnostico(),
                String.join(", ", ultimoExpediente.getMedicamentos()), ultimoExpediente.getCobro()
        );

        // Agregar la información del último expediente al textPane con formato
        StyledDocument doc = textPaneRegistros.getStyledDocument();
        SimpleAttributeSet center = new SimpleAttributeSet();
        StyleConstants.setAlignment(center, StyleConstants.ALIGN_CENTER);
        doc.setParagraphAttributes(0, doc.getLength(), center, false);
        try {
            doc.insertString(doc.getLength(), expedienteFormateado, null);
        } catch (BadLocationException e) {
            e.printStackTrace();
        }
    } else {
        // Si no hay expedientes, mostrar un mensaje
        textPaneRegistros.setText("No hay expedientes disponibles.");
    }
    }//GEN-LAST:event_btnImprimirActionPerformed

